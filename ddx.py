    def get_symptoms(self):
        print("Please enter your symptoms (comma-separated):")
        symptoms_input = input()
        symptoms = [s.strip().lower() for s in symptoms_input.split(",")]
        return symptoms

    def check_symptoms(self, symptoms):
        possible_conditions = set()
        for symptom in symptoms:
            if symptom in self.symptom_database:
                possible_conditions.update(self.symptom_database[symptom])
            else:
                print(f"Symptom '{symptom}' not recognized.")
        
        return possible_conditions

    def display_conditions(self, conditions):
        if conditions:
            print("Based on your symptoms, you may have:")
            for condition in list(conditions)[:5]:
                print(f"- {condition}")
        else:
            print("No conditions found based on the provided symptoms.")

